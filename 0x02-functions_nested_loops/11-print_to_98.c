#include <stdio.h>
#include "main.h"

/**
 * prints numbers to 98
 *Description: prints all natural number from n - 98
 * Return: o
 */
void print_to_98(int n)
{
	if (n < 98)
	{
		while (n < 98)
		{
			printf("%d, ", n);
			n++;
		}
	}
	else if (n > 98)
	{
		while (n > 98)
		{
			printf("%d, ", n);
			n--;
		}
	}
	printf("98\n");
}
